#include<stdio.h>
int main(   )
{
    int num = 3;
    printf("\n the value of num = %d ", num);
    printf("\n the value of ++num = %d ", ++num );
    printf("\n the new value of num = %d ", num);

    printf("\n the value of num = %d ", num);
    printf("\n the value of --num = %d ", --num);
    printf("\n the new value of num = %d   ", num);
    return 0;

}
